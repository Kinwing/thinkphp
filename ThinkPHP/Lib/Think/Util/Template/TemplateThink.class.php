class TemplateThink{
    /**
     +----------------------------------------------------------
     * 渲染模板输出
     +----------------------------------------------------------
     * @access public
     +----------------------------------------------------------
     * @param string $templateFile 模板文件名
     * @param array $var 模板变量
     +----------------------------------------------------------
     * @return void
     +----------------------------------------------------------
     */
    public function fetch($templateFile,$var) {
        // 缓存无效 重新编译
        $tpl = Think::instance('ThinkTemplate');
        // 编译并加载模板文件
        $tpl->load($templateFile,$var);
    }

}